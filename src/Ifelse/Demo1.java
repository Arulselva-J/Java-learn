package Ifelse;

public class Demo1 {

	public static void main(String[] args) {
		
		int no=5;
		if(no<=5) {
			
			if(no%2==0) {
				
				if(no==2) {
					
					System.out.println(no);
				}
				else {
					System.out.println(no);
				}
			}
			else {
				if(no==1) {
					System.out.println(no);
				}
				else if(no==3) {
					
					System.out.println(no);
					
				}
				else {
					System.out.println(no);
				}
			}
		}
	    

	}

}
